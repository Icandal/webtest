import React, { useState, useEffect } from 'react';
import './InformedConsentPopup.css';

const InformedConsentPopup = ({ onConsent, onDecline }) => {
  const [visible, setVisible] = useState(true);

  const handleAgree = () => {
    setVisible(false);
    if (onConsent) onConsent();
  };

  const handleDecline = () => {
    setVisible(false);
    if (onDecline) onDecline();
  };

  if (!visible) return null;

  return (
    <div className="consent-overlay">
      <div className="consent-modal">
        <h2>Уважаемые участники!</h2>
        <div className="consent-text">
          <p>
            Приглашаем вас принять участие в исследовании когнитивных функций, разработанном учеными Университета «Неймарк».
          </p>
          <p>
            Эксперимент включает несколько когнитивных тестов. Перед каждым тестом будет представлена подробная инструкция, а также несколько тренировочных заданий с обратной связью, которые помогут вам ознакомиться с процедурой выполнения.
          </p>
          <p>
            После завершения тестирования вам будет предложено ответить на несколько вопросов, касающихся вашего опыта использования современных систем искусственного интеллекта, а также на ряд общих вопросов о ваших интересах и предпочтениях.
          </p>
          <p>
            <strong>Прохождение исследования займет около 20 минут.</strong>
          </p>
          <p>
            Все данные будут храниться и обрабатываться в анонимном виде. Ваши ответы не будут связаны с вашей личностью, а факт участия в исследовании не будет доступен третьим лицам. Полученные результаты будут использоваться исключительно в научно-исследовательских целях учеными Университета «Неймарк».
          </p>
          <p>
            Участие в исследовании является добровольным. Вы можете прекратить прохождение тестирования в любой момент без объяснения причин и каких-либо негативных последствий.
          </p>
          <p>
            Пожалуйста, подтвердите свое согласие на участие, нажав кнопку ниже. Нажимая кнопку, вы подтверждаете, что ознакомились с информацией об исследовании и добровольно даете информированное согласие на участие.
          </p>
        </div>
        <div className="consent-buttons">
          <button className="consent-btn agree" onClick={handleAgree}>Согласен</button>
          <button className="consent-btn decline" onClick={handleDecline}>Не согласен</button>
        </div>
      </div>
    </div>
  );
};

export default InformedConsentPopup;